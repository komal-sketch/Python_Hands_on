# lists are ordered, mutable(changeable) collection of items.

marks = [54, 23, 67, 92]      #valid list
mixed = [42, "Hello", False, 4.2]  #valid list

# we can access the elements of lists by providing index value.

print(marks[0]) # output = 54
print(mixed[2]) # output = False

print(mixed[4]) # Error: list index out of range

# Slicing list

print(marks[2:4]) # output = [67,92]






